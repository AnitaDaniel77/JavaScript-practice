// Task 1: Create a new empty array literal
var clothes = [];

// Task 2: Add 5 clothing items using push()
clothes.push("Coat");
clothes.push("Thermal Leggings");
clothes.push("Thick Socks");
clothes.push("Turtleneck");
clothes.push("Cotton Underwear");

// Task 3: Remove the fifth item using pop()
clothes.pop(); // removes "Cotton Underwear"
console.log(clothes); // ['Coat', 'Thermal Leggings', 'Thick Socks', 'Turtleneck']

// Task 4: Add a new piece of clothing using push()
clothes.push("Bra");

// Task 5: Log the third item from the array (index 2)
console.log(clothes[2]); // Thick Socks

